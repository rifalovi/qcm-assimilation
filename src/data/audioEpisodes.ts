/** Retourne tous les thèmes distincts (ordre d'apparition). */
export const audioThemes = [...new Set(audioEpisodes.map((e) => e.themeKey))];

/** Regroupe les épisodes par thème. */
export const episodesByTheme = audioEpisodes.reduce<
  Record<AudioThemeKey, AudioEpisode[]>
>(
  (acc, ep) => {
    if (!acc[ep.themeKey]) acc[ep.themeKey] = [];
    acc[ep.themeKey].push(ep);
    return acc;
  },
  {} as Record<AudioThemeKey, AudioEpisode[]>
);

/** Regroupe les épisodes par sous-thème. */
export const episodesBySubtheme = audioEpisodes.reduce<
  Record<string, AudioEpisode[]>
>((acc, ep) => {
  if (!acc[ep.subthemeKey]) acc[ep.subthemeKey] = [];
  acc[ep.subthemeKey].push(ep);
  return acc;
}, {});

/** Trouve un épisode par son slug. */
export const getEpisodeBySlug = (slug: string): AudioEpisode | undefined =>
  audioEpisodes.find((e) => e.episodeSlug === slug);